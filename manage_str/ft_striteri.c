#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;
	int n;

	if (s && f)
	{
		n = ft_strlen(s);
		i = 0;
		while (n--)
			f(i++, s++);
	}
}
