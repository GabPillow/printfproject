#include "libft.h"

int	ft_getnumw(char const *s, char c)
{
	int	i;
	int m;

	i = 0;
	m = 0;
	while (s[i] != '\0')
	{
		while (s[i] == c && s[i] != '\0')
			i++;
		if (s[i] != '\0' && s[i] != c)
			m++;
		while (s[i] != c && s[i] != '\0')
			i++;
	}
	return (m);
}
