#include "libft.h"
#include <stdlib.h>
#include <string.h>

void	*ft_memalloc(size_t size)
{
	void *zm;

	if (!(zm = malloc(size)))
		return (NULL);
	ft_bzero(zm, size);
	return (zm);
}
